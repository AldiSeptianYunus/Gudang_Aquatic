    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        if (cIdBrg.getSelectedIndex() <= 0 || txtBarang.getText().trim().isEmpty()
            || txtJumlah.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Semua field harus diisi!");
            return;
        }

        try {
            String selectedItem = cIdBrg.getSelectedItem().toString();

            if (selectedItem.equals("Pilih Barang")) {
                JOptionPane.showMessageDialog(null, "Silakan pilih barang terlebih dahulu!");
                return;
            }

            String idBarang = selectedItem.split(" - ")[0];
            String namaBarang = txtBarang.getText().trim();
            int jumlah = Integer.parseInt(txtJumlah.getText().trim());

            if (jumlah <= 0) {
                JOptionPane.showMessageDialog(null, "Jumlah harus lebih dari 0");
                return;
            }

            if (parentForm != null) {
                parentForm.tambahItemBarang(idBarang, namaBarang, jumlah);
                JOptionPane.showMessageDialog(null, "Barang berhasil ditambahkan ke transaksi");

                refreshTableUtama();
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Error: Parent form tidak ditemukan!");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Jumlah harus berupa angka yang valid");
        } catch (ArrayIndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(null, "Format data barang tidak valid");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_bSimpanActionPerformed

