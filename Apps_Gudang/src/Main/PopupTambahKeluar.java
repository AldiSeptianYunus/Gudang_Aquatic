    private void setupCIdBarang() {
        cIdBrg.removeAllItems(); 
        cIdBrg.addItem("Pilih Barang");
        
        try {
            String query = "SELECT id_barang, nama_barang FROM barang ORDER BY nama_barang";
            PreparedStatement pst = conn.prepareStatement(query); // Gunakan PreparedStatement
            ResultSet rs = pst.executeQuery();
            
            while (rs.next()) {
                String idBarang = rs.getString("id_barang");
                String namaBarang = rs.getString("nama_barang");
                
                cIdBrg.addItem(idBarang + " - " + namaBarang);
            }
            
            rs.close();
            pst.close();
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error mengambil data: " + e.getMessage());
        }
    }
    
