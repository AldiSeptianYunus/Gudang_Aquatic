    private void cIdBrgItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cIdBrgItemStateChanged
        if (evt.getStateChange() == java.awt.event.ItemEvent.SELECTED
            && cIdBrg.getSelectedItem() != null) {

            String selectedItem = cIdBrg.getSelectedItem().toString();

            if (selectedItem.equals("Pilih Barang")) {
                txtBarang.setText("");
                return;
            }

            try {
                String idBarang = selectedItem.split(" - ")[0];

                String sql = "SELECT nama_barang FROM barang WHERE id_barang = ?";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, idBarang);
                ResultSet hasil = pst.executeQuery();

                if (hasil.next()) {
                    txtBarang.setText(hasil.getString("nama_barang"));
                } else {
                    txtBarang.setText("");
                    JOptionPane.showMessageDialog(null, "Data barang tidak ditemukan!");
                }

                hasil.close();
                pst.close();

            } catch (ArrayIndexOutOfBoundsException e) {
                txtBarang.setText("");
                JOptionPane.showMessageDialog(null, "Format data barang tidak valid");
            } catch (SQLException e) {
                txtBarang.setText("");
                JOptionPane.showMessageDialog(null, "Error mengambil data barang: " + e.getMessage());
            } catch (Exception e) {
                txtBarang.setText("");
                JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_cIdBrgItemStateChanged

